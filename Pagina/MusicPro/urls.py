from django.contrib import admin
from django.urls import path, include
from rest_framework import routers
from MusicPro.appMusicPro import views
from WebPage.views import  agregar_producto, eliminar_producto, limpiar_carrito, restar_producto

router = routers.DefaultRouter()
router.register(r'users', views.UserViewSet)
router.register(r'groups', views.GroupViewSet)
router.register(r'productos', views.ProductoViewSet)

urlpatterns = [
    path('admin/', admin.site.urls),
    path('api/', include(router.urls)),
    path('api-auth/', include('rest_framework.urls', namespace='rest_framework')),#Autentificaci√≥n de la API
    path('', include('WebPage.urls')),#frontend templates
    path('agregar/<int:producto_id>/', agregar_producto, name="Add"),
    path('eliminar/<int:producto_id>/', eliminar_producto, name="Del"),
    path('restar/<int:producto_id>/', restar_producto, name="Sub"),
    path('limpiar/', limpiar_carrito, name="CLS")
    
]
